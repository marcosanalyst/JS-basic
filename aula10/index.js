/* Aritméticos  
Adicão / concatenação
*/

const num1 = 10;
const num2 = 2;
console.log(num1 + num2);

// Potenciação **
console.log(num1 ** num2); // 10 elevado a 2

// % Módulo: resto da divisão
console.log(num1 % num2);

// Incremento ++ | Decremento -- 
let contador = 1;
console.log(contador++); //1
console.log(contador); //2

const passo = 5;
let contador2 = 0;
contador2 +=passo; // contador2 = contador2 + passo
console.log(contador2);

let contador3 = 2;
contador3 **=10;
console.log(contador3);
console.log(contador3);

// NaN - Not a Number
const n1 = 10;
const n2 = 'b';
console.log(n1 * n2);